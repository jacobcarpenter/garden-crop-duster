function nextLevel () {
    if (currentLevel < levels.length) {
        crashed = false
        tiles.destroySpritesOfKind(SpriteKind.Player)
        tiles.destroySpritesOfKind(SpriteKind.CrashedPlayer)
        tiles.destroySpritesOfKind(SpriteKind.Projectile)
        tiles.destroySpritesOfKind(SpriteKind.PesticideCloud)
        tiles.destroySpritesOfKind(SpriteKind.Building)
        tiles.destroySpritesOfKind(SpriteKind.CompletedPlant)
        tiles.destroySpritesOfKind(SpriteKind.Indicator)
        tiles.loadMap(levels[currentLevel])
        currentLevel += 1
        for (let value of houseTiles) {
            tiles.createSpritesOnTiles(value, SpriteKind.Building)
            tiles.replaceAllTiles(value, myTiles.tile0)
        }
        for (let value of plantTiles) {
            tiles.createSpritesOnTiles(value, SpriteKind.Plant)
            tiles.replaceAllTiles(value, myTiles.tile0)
        }
        startLocation = tiles.getTilesByType(myTiles.tile12)[0]
        tiles.replaceAllTiles(myTiles.tile12, myTiles.tile0)
        startPlayer()
    } else {
        game.over(true)
    }
}
